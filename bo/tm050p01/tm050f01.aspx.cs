		#region フォームを呼び出します(ボタンID : Btnenter(確定))
		/// <summary>
		/// フォームを呼び出します。
		/// ボタンID(Btnenter(確定))
		/// アクションID(FRM)
		/// の処理メソッド。
		/// </summary>
		/// <param name="sender">object</param>
		/// <param name="e">System.EventArgs</param>
		protected virtual void OnBTNENTER_FRM(object sender, System.EventArgs e)
		{
			//メソッドの開始処理を実行する。
			StartMethod(sender, e, this.GetType().Name + ".OnBTNENTER_FRM");
			//入力チェックに失敗した場合、何も処理せずに返します
			if (!base.DoValidate(sender))
			{
				//クライアントチェックエラー時、画面描画する
				SetItems();
				SetAttribute();
				return;
			}
			
			//アクションコンテキストを取得する
			IPageContext pageContext = base.GetPageContext();
			ICommandInfo commandInfo = pageContext.CommandInfo;
			
			try
			{
				//アクションロジックを処理します
				IFacadeContext facadeContext = FacadeContextFactory.GetFacadeContext(pageContext);
				InitFacadeContext(facadeContext);

				// 業務ロジック↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓
				#region 初期化
				// FormVO取得
				// 画面より情報を取得する。
				Tm050f01Form f01VO = (Tm050f01Form)facadeContext.FormVO;
				IDataList m1List = f01VO.GetList("M1");

				// 一覧の初期化
				m1List.ClearCacheData();
				m1List.Clear();
				#endregion

				#region 入力値チェック
				// 1-1 ＣＳＶ参照
				// 選択したパスに対象のファイルが存在しなかった場合、エラー
				if (string.IsNullOrEmpty(this.Csv_sansyo.FileName))
				{
					// ファイルが存在しない場合
					ErrMsgCls.AddErrMsg("E119", "CSV", facadeContext);
				}

				// 1-2 ＣＳＶ参照
				// ファイルがCSV形式でなかった場合、エラー
				if (!MessageDisplayUtil.HasError(facadeContext)
					&& !Path.GetExtension(this.Csv_sansyo.FileName).ToLower().Equals(".csv"))
				{
					// 拡張子がCSVでない場合
					ErrMsgCls.AddErrMsg("E123", string.Empty, facadeContext);
				}

				// 1-3 ＣＳＶ参照
				// ファイルが0バイトの場合、エラー
				if (!MessageDisplayUtil.HasError(facadeContext)
					&& this.Csv_sansyo.FileBytes.Length == 0)
				{
					// 0バイトの場合
					ErrMsgCls.AddErrMsg("E134", string.Empty, facadeContext);
				}

				//エラー判定
				if (MessageDisplayUtil.HasError(facadeContext))
				{
					base.SetError(pageContext);
					return;
				}
				#endregion

				// リクエストよりアップロードファイルを取得
				HttpFileCollection files = Request.Files;

				// アップロードファイル情報リストを取得
				IList<UploadInfo> list = FileManager.Upload(files, pageContext.CommandInfo.ProgramId);

				// アップロードファイル情報リストをファサードに渡す
				facadeContext.SetUserObject(Tm050p01Constant.FCDUO_UPLOAD_INFO, list);
				// 業務ロジック↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑

				new Tm050f01Facade().DoBTNENTER_FRM(facadeContext);

				//エラー判定
				if(MessageDisplayUtil.HasError(facadeContext))
				{
					base.SetError(pageContext);
					return;
				}

				// 業務ロジック↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓
				if (facadeContext.UserMap.Contains(Tm050p01Constant.FCDUO_IMPORT_INFO))
				{
					// 取込情報をセッションに設定
					Session[OpenTm050p01Cls.KEY_CSV_IMPORT_RESULT] = facadeContext.GetUserObject(Tm050p01Constant.FCDUO_IMPORT_INFO);

					// 画面終了スクリプト出力
					OpenTm050p01Cls.CloseTm050p01(Page, true);
				}
				// 業務ロジック↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑

				//遷移先の画面設定（入出力画面定義で設定した遷移先は初期化処理で設定済み）
				//入出力画面定義で設定した遷移先以外の画面に遷移する場合は以下のソースを
				//コメントインし、設定して下さい。
				//commandInfo.ToProgramId = "遷移先プログラムID";
				//commandInfo.ToFormId = "遷移先フォームID";
				
				//他の処理モードを設定する必要がある場合、次の行を修正してください
				commandInfo.ActionMode = "UPD";
				commandInfo.PageLoadMode = false;
				
				//共通アクション
				base.DoCommonAction();
			}
			catch (System.Exception ex)
			{
				ThrowException(ex, pageContext);
				return;
			}
			
			//URLクエリ追加用NameValueCollection
			NameValueCollection queryList = new NameValueCollection();
			
			//フォーカスを当てる項目を指定したい場合はFormFocusUtilを使ってqueryListに追加すること
			
			//メソッドの終了処理を実行する。
			EndMethod(sender, e, this.GetType().Name + ".OnBTNENTER_FRM");
			
			//画面遷移
			//base.Forward(pageContext, queryList);

			// 業務ロジック↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓
			if (Session[OpenTm050p01Cls.KEY_CSV_IMPORT_RESULT] == null)
			{
				//画面遷移
				base.Forward(pageContext, queryList);
			}
			// 業務ロジック↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑
		}
